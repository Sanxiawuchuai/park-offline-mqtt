package com.drzk.service.entity;

public class DisplayChar {

	private String voiceType;
	private String disValue;
	
	/**
	* voiceType.
	*
	* @return the voiceType
	* @since JDK 1.8
	*/
	public String getVoiceType() {
		return voiceType;
	}
	
	/**
	* voiceType.
	*
	* @param voiceType the voiceType to set
	* @since JDK 1.8
	*/
	public void setVoiceType(String voiceType) {
		this.voiceType = voiceType;
	}
	
	/**
	* disValue.
	*
	* @return the disValue
	* @since JDK 1.8
	*/
	public String getDisValue() {
		return disValue;
	}
	
	/**
	* disValue.
	*
	* @param disValue the disValue to set
	* @since JDK 1.8
	*/
	public void setDisValue(String disValue) {
		this.disValue = disValue;
	}
	
	
}
